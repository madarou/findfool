package zpl.oj.model.common;

public class School {
	private int id;
	private int code;
	private String name;
	private String pinyin;
	private String alp;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPinyin() {
		return pinyin;
	}
	public void setPinyin(String pinyin) {
		this.pinyin = pinyin;
	}
	public String getAlp() {
		return alp;
	}
	public void setAlp(String alp) {
		this.alp = alp;
	}
	
	
}
