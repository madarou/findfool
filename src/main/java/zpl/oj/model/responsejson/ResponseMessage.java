package zpl.oj.model.responsejson;

public class ResponseMessage {

	private String msg;
	private String handler_url;
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getHandler_url() {
		return handler_url;
	}
	public void setHandler_url(String handler_url) {
		this.handler_url = handler_url;
	}
	
	
}
